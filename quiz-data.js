// ═══ 洗牌 + 抽取函数 ═══
// Fisher-Yates 洗牌，每次抽取 TOTAL_QUESTIONS 道题
var _qsPool = null;
function getQuestions(){
  if(_qsPool) return _qsPool;
  var pool = QS.slice();
  for(var i=pool.length-1;i>0;i--){
    var j=Math.floor(Math.random()*(i+1));
    var tmp=pool[i];pool[i]=pool[j];pool[j]=tmp;
  }
  _qsPool = pool.slice(0, TOTAL_QUESTIONS);
  return _qsPool;
}
