function getCompareList(pt, allTypes) {
  var result = [];
  var myKey = pt.cn || pt.name || pt.t;
  var keys = Object.keys(allTypes);

  // 找一个人格相似的（余弦相似度最高，排除自己）
  var similar = null, simScore = -Infinity;
  keys.forEach(function(k) {
    if (k === myKey) return;
    var t = allTypes[k];
    var dot = 0, dLen = 0, tLen = 0;
    var td = {};
    if (t.dcols) {
      t.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        td[kv[0]] = parseFloat(kv[1]);
      });
    }
    if (pt.dcols) {
      pt.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        var dv = parseFloat(kv[1]);
        dLen += dv * dv;
        if (td[kv[0]] !== undefined) {
          dot += dv * td[kv[0]];
          tLen += td[kv[0]] * td[kv[0]];
        }
      });
    }
    var cos = (dLen > 0 && tLen > 0) ? dot / (Math.sqrt(dLen) * Math.sqrt(tLen)) : 0;
    if (cos > simScore) { simScore = cos; similar = t }
  });

  if (similar) {
    result.push({
      type: 'similar',
      label: '相似人格',
      icon: '🔮',
      name: similar.cn || similar.name || similar.t,
      t: similar.t,
      tags: similar.tags || [],
      desc: COMPARE_TIPS.similar[Math.floor(Math.random() * COMPARE_TIPS.similar.length)]
    });
  }

  // 找一个人格相反的（余弦相似度最低，排除自己和相似的）
  var simKey = similar ? (similar.cn || similar.name || similar.t) : null;
  var opposite = null, oppScore = Infinity;
  keys.forEach(function(k) {
    if (k === myKey || k === simKey) return;
    var t = allTypes[k];
    var dot = 0, dLen = 0, tLen = 0;
    var td = {};
    if (t.dcols) {
      t.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        td[kv[0]] = parseFloat(kv[1]);
      });
    }
    if (pt.dcols) {
      pt.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        var dv = parseFloat(kv[1]);
        dLen += dv * dv;
        if (td[kv[0]] !== undefined) {
          dot += dv * td[kv[0]];
          tLen += td[kv[0]] * td[kv[0]];
        }
      });
    }
    var cos = (dLen > 0 && tLen > 0) ? dot / (Math.sqrt(dLen) * Math.sqrt(tLen)) : 0;
    if (cos < oppScore) { oppScore = cos; opposite = t }
  });

  if (opposite) {
    result.push({
      type: 'opposite',
      label: '相反人格',
      icon: '🪞',
      name: opposite.cn || opposite.name || opposite.t,
      t: opposite.t,
      tags: opposite.tags || [],
      desc: COMPARE_TIPS.opposite[Math.floor(Math.random() * COMPARE_TIPS.opposite.length)]
    });
  }

  // 找一个人格互补的（维度差异在40~65之间，排除自己、相似、相反）
  var oppKey = opposite ? (opposite.cn || opposite.name || opposite.t) : null;
  var comp = null, compScore = -Infinity;
  keys.forEach(function(k) {
    if (k === myKey || k === simKey || k === oppKey) return;
    var t = allTypes[k];
    var score = 0;
    var td = {};
    if (t.dcols) {
      t.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        td[kv[0]] = parseFloat(kv[1]);
      });
    }
    if (pt.dcols) {
      pt.dcols.forEach(function(dc) {
        var kv = dc.split(':');
        var myVal = parseFloat(kv[1]);
        if (td[kv[0]] !== undefined) {
          var diff = Math.abs(myVal - td[kv[0]]);
          if (diff >= 40 && diff <= 65) score += (65 - Math.abs(52.5 - diff)) * 1.2;
          else score += Math.max(0, 20 - diff * 0.3);
        }
      });
    }
    if (score > compScore) { compScore = score; comp = t }
  });

  if (comp) {
    result.push({
      type: 'complement',
      label: '互补人格',
      icon: '🧩',
      name: comp.cn || comp.name || comp.t,
      t: comp.t,
      tags: comp.tags || [],
      desc: COMPARE_TIPS.complement[Math.floor(Math.random() * COMPARE_TIPS.complement.length)]
    });
  }

  return result;
}

